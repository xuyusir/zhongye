package jy.zhongye.entity.common;

public enum ResultEnum {

    //all Enum
    FAIL(10001, 0, "fail"),
    SUCCESS(10000,1,"SUCCESS");

    //code
    private Integer code;
    private Integer status;
    private String msg;

    //construct function
    ResultEnum(Integer code, Integer status, String msg) {
        this.code = code;
        this.status = status;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public Integer getStatus() {
        return status;
    }

    public String getMsg() {
        return msg;
    }
}
